// Initialize localStorage with mock data.
// Uses a version key â€” if the version doesn't match, all data is re-seeded.
// Bump MOCK_DATA_VERSION whenever you want to force a fresh reset.
const MOCK_DATA_VERSION = 'v2';

export function initializeMockData() {
    const storedVersion = localStorage.getItem('styleswap_data_version');

    if (storedVersion !== MOCK_DATA_VERSION) {
        // Clear everything and re-seed
        localStorage.removeItem('styleswap_users');
        localStorage.removeItem('styleswap_products');
        localStorage.removeItem('styleswap_orders');
        localStorage.removeItem('styleswap_currentUser');
        // Clear all per-user carts
        Object.keys(localStorage)
            .filter(k => k.startsWith('styleswap_cart_'))
            .forEach(k => localStorage.removeItem(k));

        localStorage.setItem('styleswap_users', JSON.stringify(INITIAL_USERS));
        localStorage.setItem('styleswap_products', JSON.stringify(INITIAL_PRODUCTS));
        localStorage.setItem('styleswap_orders', JSON.stringify(INITIAL_ORDERS));
        localStorage.setItem('styleswap_data_version', MOCK_DATA_VERSION);
    }
}
